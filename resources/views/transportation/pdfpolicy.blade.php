<p class=Indent><span lang=EN-GB>B.   For Insurances written for more or less
than one year:-</span></p>

<p class=Indent1><span lang=EN-GB>1.   If insurance has been in force for 12
months or less, apply the standard short rate table for annual insurances to
the full annual premium determined as for an insurance written for a term of
one year.</span></p>

<p class=Indent1><span lang=EN-GB>2.   If insurance has been in force for more
than 12 months:</span></p>

<p class=Indent2>a.   Determine full annual premium as for an insurance written
for a term of one year.</p>

<p class=Indent2>b.   Deduct such premium from the full insurance premium, and
on the remainder calculate the pro rata Earned Premium on the basis of the
ratio of the length of time beyond one year the insurance has been in force to
the length of time beyond one year for which the insurance was originally
written.</p>

<p class=Indent2>c.   Add premium produced in accordance with items (a) and (b)
to obtain Earned Premium during full period insurance has been in force.</p>

