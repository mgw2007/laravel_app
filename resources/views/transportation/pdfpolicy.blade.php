<p class=MainHead>LLOYD’S AUTOMOBILE PHYSICAL DAMAGE INSURANCE</p>

<p class=MainHead>INSURING AGREEMENTS</p>

<p class=Indent><span lang=EN-GB>1.   In consideration of the premium paid
hereon and the particulars and statements contained in the written Proposal, a
copy of which is attached hereto, which particulars and statements are
warranted by the Assured to be true and are agreed to be incorporated herein,
the Underwriters hereby agree to indemnify the Assured against direct and
accidental loss of or damage to the Automobiles specified in the Schedule herein,
during the Period of Insurance specified in the Schedule, while such
Automobiles are within the United States of America (excluding Hawaii, the
Philippine Islands, the Virgin Islands and Puerto Rico) and the Dominion of
Canada.</span></p>

<p class=Indent><span lang=EN-GB>2.   This Insurance covers only such and so
many of the Perils named in the Schedule as are indicated by a specific premium
set thereunder.  The Limit of the Underwriters' Liability in respect of each of
such Perils is the amount insured stated in the Schedule or the actual cash
value of the vehicle concerned at the time of loss, whichever is the less.</span></p>

<p class=Flush1>PROVIDED ALWAYS THAT Underwriters' Liability shall not exceed: </p>

<p class=Indent1><span lang=EN-GB>(a)   the limits stated in Part B of the
Schedule in respect of any combination of Automobile, truck, tractor, trailer
or semi-trailer, or</span></p>

<p class=Indent1><span lang=EN-GB>(b)  the limit stated in Part B of the
Schedule in respect of any one event, catastrophe or terminal loss.</span></p>

<p class=MsoNormal align=center style='margin-top:12.0pt;text-align:center'><b>DEFINITIONS</b></p>

<p class=Indent><span lang=EN-GB>1.   DEFINITION OF AUTOMOBILE.  The word
&quot;Automobile&quot; wherever used herein shall mean each motor vehicle or
trailer or semi-trailer described in this Insurance, including its equipment
and other equipment permanently attached thereto.  The terms of this Insurance
and the Limits of Liability, including any deductible provisions, shall apply
to each Automobile separately.</span></p>

<p class=Indent><span lang=EN-GB>2.   DEFINITION OF PERILS.</span></p>

<p class=Indent1><span lang=EN-GB>SECTION A.  FIRE, LIGHTNING AND
TRANSPORTATION.</span></p>

<p class=Flush1>This Section covers </p>

<p class=Indent1><span lang=EN-GB>(i)   loss or damage resulting from fire
arising from any accidental cause, and lightning, </span></p>

<p class=Indent1><span lang=EN-GB>(ii)  damage by smoke or smudge due to a
sudden, unusual and faulty operation of any fixed heating equipment serving the
premises in which the Automobile is located, and </span></p>

<p class=Indent1><span lang=EN-GB>(iii) loss or damage resulting from the
stranding, sinking, burning, collision or derailment of any conveyance in or
upon which the Automobile is being transported on land or on water, including
general average and salvage charges for which the Assured is legally liable.</span></p>

<p class=Indent1><span lang=EN-GB>SECTION B.  THEFT, ROBBERY AND PILFERAGE.</span></p>

<p class=Indent1><span lang=EN-GB>SECTION C.  COLLISION OR UPSET.</span></p>

<p class=Flush1>This Section covers loss of or damage to an Automobile caused by
accidental collision of the Automobile with another object, or by upset,
provided always that the deductible specified in the Schedule shall be deducted
from the amount of each and every loss or damage to each Automobile.</p>

<p class=Indent1><span lang=EN-GB>SECTION D.  WINDSTORM, EARTHQUAKE, EXPLOSION,
HAIL OR WATER. </span></p>

<p class=Flush1>This Section covers loss or damage caused by windstorm, hail,
earthquake, explosion, external discharge or leakage of water, except loss or
damage resulting from rain, snow or sleet, whether or not wind-driven.</p>

<p class=Indent1><span lang=EN-GB>SECTION E.  COMBINED ADDITIONAL COVERAGE.</span></p>

<p class=Flush1>This Section covers loss or damage caused by windstorm, hail,
earthquake, explosion, riot or civil commotion or the forced landing or falling
of any aircraft or its parts or equipment, flood or rising waters, external
discharge or leakage of water, except loss or damage resulting from rain, snow
or sleet, whether or not wind-driven.</p>

<p class=Indent1><span lang=EN-GB>SECTION F.  COMPREHENSIVE COVERAGE EXCEPT BY
COLLISION OR UPSET.</span></p>

<p class=Flush1>This Section covers loss of or damage to the Automobile except
loss or damage caused by collision of the Automobile with another object or by
upset of the Automobile or by collision of the Automobile with an Automobile to
which it is attached.  Breakage of glass and loss or damage caused by missiles,
falling objects, fire, theft, explosion, earthquake, windstorm, hail, water,
flood, vandalism, riot or civil commotion shall not be deemed loss caused by
collision or upset.</p>

<p class=MsoNormal align=center style='margin-top:12.0pt;text-align:center'><b>EXCLUSIONS</b></p>

<p class=Flush style='margin-top:12.0pt'><span style='font-family:"Times",serif'>This
Insurance does not cover </span></p>

<p class=Indent><span lang=EN-GB>1.   loss of or damage to any radio
transmitting or receiving set and tape recorders unless permanently attached to
an Insured Automobile, radio tubes in any event, robes, wearing apparel,
personal effects, or other property of the Assured or of others carried in or
upon the Automobile;</span></p>

<p class=Indent><span lang=EN-GB>2.   loss of or damage to tyres unless damaged
by fire or stolen or unless lost or damaged in an accidental collision or upset
which also caused other damage to the Insured Automobile:</span></p>

<p class=Indent><span lang=EN-GB>3.   loss or damage directly or indirectly
occasioned by, happening through or in consequence of war, invasion, acts of foreign
enemies, hostilities (whether war be declared or not), civil war, rebellion,
revolution, insurrection, military or usurped power or confiscation or
requisition or destruction or damage by or under the order of any government or
public or local authority, or, except under Sections E and F, riot or civil
commotion;</span></p>

<p class=Indent><span lang=EN-GB>4.   loss or damage arising from nuclear
reaction, nuclear radiation or radioactive contamination;</span></p>

<p class=Indent><span lang=EN-GB>5.   loss of or damage to any Automobile </span></p>

<p class=Indent1><span lang=EN-GB>(i)   while used for any purposes other than
those specified in the Schedule,</span></p>

<p class=Indent1><span lang=EN-GB>(ii)  while operated, maintained or used by
any person in violation of State Law as to age or by any person under the age
of eighteen years in any event,</span></p>

<p class=Indent1><span lang=EN-GB>(iii) while operated, maintained or used in
any race or speed contest,</span></p>

<p class=Indent1><span lang=EN-GB>(iv) while rented or used for livery purposes
or to carry passengers for a consideration, express or implied, unless
specifically agreed herein,</span></p>

<p class=Indent1><span lang=EN-GB>(v)  while subject to any bailment lease,
conditional sale, mortgage or other encumbrance, not specifically declared and described
in this Insurance,</span></p>

<p class=Indent1><span lang=EN-GB>(vi) while the Automobile is used in
connection with any illicit trade or transportation,</span></p>

<p class=Indent1><span lang=EN-GB>(vii) which is due and confined to wear and
tear, freezing, mechanical or electrical breakdown or failure, unless such
damage is the result of other losses covered by this Insurance;</span></p>

<p class=Indent><span lang=EN-GB>6.   under Sections B and F</span></p>

<p class=Indent1><span lang=EN-GB>(a)   loss or damage caused by any person or
persons in the Assured's household or in the Assured's service or employment,
whether the loss or damage occurs during the hours of such service or
employment or not,</span></p>

<p class=Indent1><span lang=EN-GB>(b)  loss suffered by the Assured as the
result of voluntarily parting with title or possession, whether or not induced
so to do by any fraudulent scheme, trick, device or false pretence,</span></p>

<p class=Indent1><span lang=EN-GB>(c)   the theft, robbery or pilferage of tools
or repair equipment except in conjunction with the theft of an entire
Automobile,</span></p>

<p class=Indent1><span lang=EN-GB>(d)  the wrongful conversion, embezzlement or
secretion by a mortgagee, vendee, lessee or other person in lawful possession
of the insured property under a mortgage, conditional sale, lease or other
contract or agreement, whether written or verbal.</span></p>

<p class=MsoNormal align=center style='margin-top:12.0pt;text-align:center'><b>CONDITIONS</b></p>

<p class=Indent><span lang=EN-GB>1.   LIMITATION OF USE.  It is understood and
agreed that the regular and frequent use of the vehicles covered hereunder is
and will be confined during the Period of this Insurance to the territory
within the radius of miles stated in the Schedule of the place of principal
garaging of such vehicles; that is, regular or frequent trips will NOT be made
during the Period of this Insurance to any location beyond such radius of the
place of principal garaging of such vehicle.</span></p>

<p class=Indent><span lang=EN-GB>2.   AUTOMATIC INSURANCE FOR NEWLY ACQUIRED
AUTOMOBILES.  If the Assured who is the owner of the Insured Automobiles
acquires ownership of another Automobile, such coverage as is afforded by this
Insurance shall apply also to such other Automobiles from the date of delivery
thereof, subject to the following additional conditions:-</span></p>

<p class=Indent1><span lang=EN-GB>(a)   If the Underwriters insure all
Automobiles owned by the Assured at the date of such delivery, this Insurance
