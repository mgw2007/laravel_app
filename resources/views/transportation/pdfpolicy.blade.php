business of the Assured but only to the extent applicable to all such
previously owned Automobiles.</span></p>

<p class=Indent1><span lang=EN-GB>(b)  If the Underwriters do not insure all
Automobiles owned by the Assured at the date of such delivery, this Insurance
applies to such other Automobile if it replaces an Automobile described in this
Insurance but only to the extent applicable to the replaced Automobile.</span></p>

<p class=Indent1><span lang=EN-GB>(c)   The coverage afforded hereunder upon
the replaced Automobile </span></p>

<p class=Indent2>(i)  automatically terminates at the date of such delivery,</p>

<p class=Indent2>(ii) does not apply to any loss or damage against which the
Assured has other valid and collectible insurance,</p>

<p class=Indent2>(iii) does not apply unless the Assured notifies the
Underwriters within ten (10) days following the date of delivery of the new
Automobile and pays any additional premium required.</p>

<p class=Indent><span lang=EN-GB>3.   NOTICE TO UNDERWRITERS.  Upon the
occurrence of any accident claimed to be covered under this Insurance, the
Assured or someone on his behalf shall give, as soon as reasonably possible,
written notice thereof to the Underwriters and in the event of theft, larceny,
robbery or pilferage or vandalism to the police, but shall not, except at his
own cost, offer or pay any reward for recovery of the vehicle.  Such notice shall
contain particulars sufficient to identify the Insured Automobile(s).</span></p>

<p class=Indent><span lang=EN-GB>4.   INSPECTION OF LOSS OR DAMAGE.  In the
event of any loss or damage covered hereunder, the Assured shall give the
Underwriters a reasonable time and opportunity to examine the Insured
Automobile before any repairs are begun or any physical evidence of damage
removed.</span></p>

<p class=Indent><span lang=EN-GB>5.   PROOF OF LOSS.  Within sixty (60) days
after loss or damage, unless such time is extended in writing by the
Underwriters, the Assured shall forward to the Underwriters a statement, signed
and sworn to by the Assured, stating the place, time and cause of the loss or
damage, the interest of the Assured and of all others in the property, the
sound value thereof and the amount of loss or damage thereto, all encumbrances
thereon and all other insurance, whether valid and collectable or not, covering
said property.  The Assured, as often as required, shall submit to examination
under oath by any person designated by the Underwriters and subscribe the
same.  As often as required, the Assured shall produce for examination all
books of accounts, bills, invoices, and other vouchers, or certified copies
thereof if the originals are lost, at such reasonable place as may be
designated by the Underwriters, and shall permit extracts and copies thereof to
be made.</span></p>

<p class=Indent><span lang=EN-GB>6.   PAYMENT OF LOSS.  The loss shall in no
event become payable, until sixty (60) days after the verified proof of loss
herein required shall have been received by the Underwriters and, if appraisal
is demanded, then not until sixty (60) days after an award has been made by the
appraisers.</span></p>

<p class=Flush1>Loss, if any, shall be payable as interest may appear to the
Assured and to the person or persons specified in the Schedule for the purpose.</p>

<p class=Indent><span lang=EN-GB>7.   PARTIAL LOSS.  In the event of partial
loss or damage under this Insurance, the Underwriters shall be liable only for
the actual cost of (and shall have the option of) repairing, rebuilding or, if
necessary, replacing the parts damaged or destroyed. </span></p>

<p class=Flush1>In the event of loss of or damage to the Automobiles described
herein, whether such loss or damage is covered by this Insurance or not the
liability of the Underwriters shall be reduced by the amount of loss or damage
until repairs have been completed.</p>

<p class=Indent><span lang=EN-GB>8.   ABANDONMENT - RETURN OF STOLEN PROPERTY. 
It shall be optional with the Underwriters to take all or any part of the
property at the agreed or appraised value, but there can be no abandonment
thereof to the Underwriters.  If theft is covered hereunder and stolen property
is recovered prior to any payment hereunder for such property, the Assured
shall take back the recovered property if so required by the Underwriters, who
will only be liable, subject to the terms, limits and conditions of this
Insurance, for any damage done to such property by the thief or thieves.</span></p>

<p class=Indent><span lang=EN-GB>9.   PROTECTION OF SALVAGE.  In the event of
any loss or damage, whether covered hereunder or not, the Assured shall protect
the property from other or further loss or damage, and any such other or
further loss or damage due directly or indirectly to the Assured's failure to
protect shall not be recoverable hereunder.  Any such act of the Assured or the
Underwriters in recovering, saving and preserving the property described
herein, shall be considered as done for the benefit of all concerned and
without prejudice to the rights of either party, and where the loss or damage
suffered constitutes a claim hereunder, then all reasonable expenses thus
incurred shall also constitute a claim hereunder, provided, however, that the
Underwriters shall not be responsible for the payment of any reward offered for
the recovery of the insured property unless authorized by the Underwriters.</span></p>

<p class=Indent><span lang=EN-GB>10.  OTHER INSURANCE.  If the Assured carries
a policy of another insurer against a loss covered hereby, the Assured shall
not be entitled to recover from the Underwriters a larger proportion of the
entire loss than the amount hereby insured bears to the total amount of valid
and collectible insurance, and if any person, firm or corporation other than
the Assured has valid and collectible insurance against any loss covered hereby
then no such person, firm or corporation shall be considered as an Assured
hereunder.</span></p>

<p class=Indent><span lang=EN-GB>11.  APPRAISAL.  In case the Assured and
Underwriters shall fail to agree as to the amount of loss or damage each shall
on the written demand of either, select a competent and disinterested
appraiser.  Before entering upon the reference, the appraisers shall first
select a competent and disinterested umpire, and failing for fifteen (15) days
